#include "ft_printf.h"

int	ft_find(char *s, int i, char c)
{
	int count;

	count = 0;
	while (s[i] != '\0' && ft_find_types(s[i], -1) != 1)
	{
		if (s[i] == c)
			count++;
		i++;
	}
	return (count);
}
